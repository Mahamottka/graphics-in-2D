package utilities;

public class IntPair {
    int x, y;

    //čistá implementace vektoru, kombinace X,Y

    public IntPair(int x, int y) {
        this.x = x;
        this.y = y;
    }
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
